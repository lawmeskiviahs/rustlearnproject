pub fn run() {
    // print to console
    println!("Hello World!");

    // basic formatting
    println!("My name is {}", "Shaivik");

    // positional arguments
    println!("{0} is a {1} person and {0} is also {2}", "Shivam", "nice", "lazy");

    // named arguments
    println!("{name} plays {game}", name="Boni", game="nothing");

    // basic maths
    println!("10 + 10 = {}", 10+10);
}
